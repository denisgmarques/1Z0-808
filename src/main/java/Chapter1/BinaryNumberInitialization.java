public class BinaryNumberInitialization {
   public static void main(String[] args) {
      int a = 0b101;
      int b = 0xE;
      double c = 0xE;


      System.out.println("int as 0b101");
      System.out.println(a);
      System.out.println("int as 0xE");
      System.out.println(b);
      System.out.println("double as 0xE");
      System.out.println(c);
   }
}
