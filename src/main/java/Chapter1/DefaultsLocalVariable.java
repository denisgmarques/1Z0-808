public class DefaultsLocalVariable {

   public static void main(String[] args) {
      double db;
      int it;
      String st;
      boolean bl;
      float fl;
      Object ob;
      char ch;

      System.out.println("double default is " + db);
      System.out.println("int default is " + it);
      System.out.println("String default is " + st);
      System.out.println("boolean default is " + bl);
      System.out.println("float default is " + fl);
      System.out.println("Object default is " + ob);
      System.out.println("char default is " + ch);
      System.out.println("char code of default char is " + (int) ch);
   }
}
